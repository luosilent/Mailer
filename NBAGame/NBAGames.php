 * @return  string
 */

$appkey = "";

//NBA常规赛
$url = "http://op.juhe.cn/onebox/basketball/nba";
$params = array(
    "key" => $appkey,
    "dtype" => "json",
);
$paramstring = http_build_query($params);
$content = juhecurl($url,$paramstring);
$result = json_decode($content,true);
if($result){
    if($result['error_code']=='0'){
        print_r($result);
    }else{
        echo $result['error_code'].":".$result['reason'];
    }
}else{
    echo "请求失败";
}

